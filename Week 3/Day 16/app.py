if __name__ == '__main__':
    """
    КАК РАБОТАЕТ:
    
    if __name__ == '__main__':
    Проверяет: запущен ли файл напрямую?
    
    Если да - запускаем сервер
    Если файл импортирован - не запускаем
    """
    
    app.run(debug=True)
    """
    app.run() запускает встроенный сервер
    
    ПАРАМЕТРЫ:
    debug=True - режим отладки
    
    ЧТО ДАЁТ debug=True:
    1. Автоперезагрузка при изменении кода
    2. Подробные ошибки в браузере
    3. Интерактивный отладчик
    
    ВАЖНО:
    debug=True только для разработки!
    На продакшене должен быть False
    
    СЕРВЕР ЗАПУСТИТСЯ:
    http://127.0.0.1:5000/
    или
    http://localhost:5000/
    
    127.0.0.1 = localhost = ваш компьютер
    5000 = порт (номер двери)
    """    